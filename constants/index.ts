    `You are an expert in ATS (Applicant Tracking System) and resume analysis.
      Please analyze and rate this resume and suggest how to improve it.
      The rating can be low if the resume is bad.
      Be thorough and detailed. Don't be afraid to point out any mistakes or areas for improvement.
      If there is a lot to improve, don't hesitate to give low scores. This is to help the user to improve their resume.
      If available, use the job description for the job user is applying to to give more detailed feedback.
      If provided, take the job description into consideration.
      The job title is: ${jobTitle}
      The job description is: ${jobDescription}
      
      IMPORTANT: 
      - In the keywordMatch section, extract important keywords/skills from the job description and check which ones appear in the resume (found) and which are missing (missing). If no job description is provided, use common keywords for the job title.
      - In the improvementSuggestions section, pick 3-5 actual bullet points or phrases from the resume and rewrite them to be more impactful, quantified, and tailored to the job description. Each suggestion should have the original text, improved version, and a brief reason.
      
      Provide the feedback using the following format:
      ${AIResponseFormat}
      Return the analysis as an JSON object, without any other text and without the backticks.
      Do not include any other text or comments.`;

